package cr.ac.itcr;

public class Calculator {

    public int sum(int a, int b) {
        return a+b;
    }

    public float sum(float a, float b) {
        return a+b;
    }

    public int multiply(int a, int b) {
        return a*b;
    }

    public float multiply(float a, float b) {
        return a*b;
    }

    public float divide(float a, float b) {
        return a/b;
    }
}
